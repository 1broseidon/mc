// RunClickText composes find_text + click on the highest-confidence
// candidate. TARGET_NOT_FOUND when nothing matched; TARGET_AMBIGUOUS
// when strict and multiple candidates pass min_confidence.
func RunClickText(ctx context.Context, action Action) (contract.ActionResult, error) {
	bctx := imageutil.NewBatchContext()
	result, err := runFindText(ctx, bctx, action)
	if err != nil {
		return contract.ActionResult{Action: "click_text", OK: false}, err
	}
	cand, err := pickClickCandidate(result.Candidates, action.MinConfidence, action.Strict, "text")
	if err != nil {
		return contract.ActionResult{Action: "click_text", OK: false}, err
	}
	if err := input.Click(ctx, input.ClickRequest{Point: centerOf(cand), Button: action.Button, Count: max1(action.Count)}); err != nil {
		return contract.ActionResult{Action: "click_text", OK: false}, err
	}
	return contract.ActionResult{Action: "click_text", OK: true, Backend: "XTest", Details: map[string]any{"matched": cand}}, nil
}

