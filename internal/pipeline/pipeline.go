func Observe(ctx context.Context, includeScreenshot bool) (contract.ObserveResult, error) {
	info, err := screen.Info(ctx)
	if err != nil {
		return contract.ObserveResult{}, err
	}
	wins, err := window.List(ctx)
	if err != nil {
		return contract.ObserveResult{}, err
	}
	focused, _ := window.Focused(ctx)
	cursor, _ := screen.Cursor(ctx)
	// Pass the X11 window list into the AT-SPI walker so top-level
	// frames can be correlated to their X11 window_id (task-11). The
	// a11y package itself can't import internal/window without a
	// layering cycle, so the orchestrator threads the snapshot
	// through. Gio/ImGui apps don't expose AT-SPI elements and so
	// stay uncorrelated — no false positives.
	tree, _ := a11y.TreeWithWindows(ctx, 8, wins)
	out := contract.ObserveResult{
		Screen:        info,
		Windows:       wins,
		FocusedWindow: focused,
		Cursor:        cursor,
		Accessibility: tree,
		Backends: map[string]string{
			"screen": "x11",
			"window": "EWMH",
			"input":  "XTest",
		},
	}
	if includeScreenshot {
		shot, err := screen.Capture(ctx, screen.CaptureRequest{MaxEdge: 1568})
		if err != nil {
			return out, err
		}
		out.Screenshot = &shot
	}
	return out, nil
}
