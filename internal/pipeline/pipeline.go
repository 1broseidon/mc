func pickClickCandidate(cands []contract.FindCandidate, minConf float64, strict bool, what string) (contract.FindCandidate, error) {
	if len(cands) == 0 {
		return contract.FindCandidate{}, contract.NotFound("TARGET_NOT_FOUND", "click target not found", map[string]any{"what": what})
	}
	if strict {
		var passing []contract.FindCandidate
		for _, c := range cands {
			if c.Confidence >= minConf {
				passing = append(passing, c)
			}
		}
		if len(passing) > 1 {
			return contract.FindCandidate{}, contract.Precondition("TARGET_AMBIGUOUS", "multiple candidates above min_confidence", map[string]any{"what": what, "count": len(passing), "min_confidence": minConf})
		}
		if len(passing) == 1 {
			return passing[0], nil
		}
	}
	return cands[0], nil
}

func centerOf(c contract.FindCandidate) contract.Point {
	return contract.Point{
		X:     c.Bounds.X + c.Bounds.Width/2,
		Y:     c.Bounds.Y + c.Bounds.Height/2,
		Space: contract.CoordSpaceScreen,
	}
}

func resolvePoint(point contract.Point, slot string, bctx *imageutil.BatchContext) (contract.Point, error) {
	if slot == "" {
		return point, nil
	}
	cand, ok := bctx.Slot(slot)
	if !ok {
		return contract.Point{}, contract.Validation("TARGET_SLOT_UNKNOWN", "target_slot does not reference a stored find result", map[string]any{"target_slot": slot})
	}
	return centerOf(cand), nil
}

func storeSlot(bctx *imageutil.BatchContext, name string, result contract.FindResult) {
	if name == "" || len(result.Candidates) == 0 {
		return
	}
	bctx.SetSlot(name, result.Candidates[0])
}

func pointZero(p contract.Point) bool {
	return p.X == 0 && p.Y == 0 && p.Space == "" && p.CoordMap == "" && p.Target.Empty() && p.MonitorIndex == nil
}

// windowActionResult packages a window-verb result into the standard
// contract.ActionResult envelope, surfacing the post-op window snapshot
// and any non-fatal warning (e.g. WINDOW_GEOMETRY_REFUSED) under
// details. Result.OK is always true; warnings are advisory.
func windowActionResult(action string, res window.VerbResult) contract.ActionResult {
	details := map[string]any{"window": res.Window}
	if res.Warning != nil {
		details["warning"] = res.Warning
	}
	if len(res.Notes) > 0 {
		details["notes"] = res.Notes
	}
	return contract.ActionResult{Action: action, OK: true, Backend: "x11.EWMH", Details: details}
}

func max1(n int) int {
	if n <= 0 {
		return 1
	}
	return n
}

