// TestFindText_MultiWordPhrase covers v0.3 epic-2 task-15: matching a
// multi-word query against the joined text of consecutive OCR tokens.
// Single-token literal queries must still produce byte-identical
// candidate streams (backward-compat constraint), so the regression
// guard is the same fixture & query as TestFindText_HappyPath.
func TestFindText_MultiWordPhrase(t *testing.T) {
	// (a) Two-word query against the hello_world fixture: tokens are
	//     "hello" and "world"; "hello world" must hit as a phrase
	//     candidate whose bounds span both tokens.
	if tesseractAvailable() {
		img := loadFixturePNG(t, "hello_world.png")
		region := contract.Bounds{X: 0, Y: 0, Width: img.Bounds().Dx(), Height: img.Bounds().Dy()}
		res, err := FindText(context.Background(), nil, region, img, FindTextRequest{Query: "hello world"})
		if err != nil {
			t.Fatalf("two-word FindText error: %v", err)
		}
		if len(res.Candidates) == 0 {
			t.Fatalf("expected >=1 candidate for 'hello world', got 0")
		}
		// Top candidate should be the phrase: token count >= 2 and
		// bounds wider than either single token.
		top := res.Candidates[0]
		if tc, _ := top.Extra["tokens"].(int); tc < 2 {
			t.Errorf("top candidate tokens=%v, want >=2 (phrase match)", top.Extra["tokens"])
		}
		gotText, _ := top.Extra["text"].(string)
		// Phrase text is "hello world" (case-insensitive substring
		// match is the default, so casing depends on what tesseract
		// emitted — assert via lowercase contains).
		if !strings.Contains(strings.ToLower(gotText), "hello world") {
			t.Errorf("top candidate text=%q, want it to contain 'hello world'", gotText)
		}
		// Width must span more than a single token (~94px) — both
		// tokens together are ~191px wide in the fixture.
		if top.Bounds.Width < 150 {
			t.Errorf("phrase bounds too narrow: width=%d, want >=150 (spanning both tokens)", top.Bounds.Width)
		}

		// (c) Regression: single-word query produces the same shape as
		//     the v0.2 baseline (TestFindText_HappyPath asserts the
		//     same invariants). Re-running here ensures phrase mode
		//     stayed off and no duplicate candidates were emitted.
		resSingle, err := FindText(context.Background(), nil, region, img, FindTextRequest{Query: "hello"})
		if err != nil {
			t.Fatalf("single-word FindText error: %v", err)
		}
		// Should be exactly one candidate for "hello" — the token
		// match. No phrase candidate (multiwordMode is gated on
		// whitespace in query).
		if len(resSingle.Candidates) != 1 {
			t.Errorf("single-word 'hello': got %d candidates, want 1 (no phrase duplication)", len(resSingle.Candidates))
		}
		if len(resSingle.Candidates) > 0 {
			if _, hasTokens := resSingle.Candidates[0].Extra["tokens"]; hasTokens {
				t.Errorf("single-word candidate should not carry 'tokens' extra: %+v", resSingle.Candidates[0].Extra)
			}
		}
	}

	// (b) Three-word phrase via synthetic OCR tokens — exercises
	//     phraseMatchUnits independent of tesseract. Tokens lie on the
	//     same (block, par, line) so the joiner reaches len=3.
	words := []ocrWord{
		{text: "Loaded", left: 10, top: 5, width: 50, height: 14, confidence: 0.95, block: 1, par: 1, line: 1},
		{text: "skill", left: 65, top: 5, width: 40, height: 14, confidence: 0.93, block: 1, par: 1, line: 1},
		{text: "anvil", left: 110, top: 5, width: 45, height: 14, confidence: 0.91, block: 1, par: 1, line: 1},
		// Next line, must NOT be joined into the above phrase.
		{text: "Other", left: 10, top: 25, width: 50, height: 14, confidence: 0.9, block: 1, par: 1, line: 2},
	}
	phrases := phraseMatchUnits(words, maxPhraseTokens)
	var got3 *matchUnit
	for i := range phrases {
		if phrases[i].text == "Loaded skill anvil" {
			got3 = &phrases[i]
			break
		}
	}
	if got3 == nil {
		var texts []string
		for _, p := range phrases {
			texts = append(texts, p.text)
		}
		t.Fatalf("three-word phrase 'Loaded skill anvil' not generated; got phrases: %v", texts)
	}
	if got3.tokenCount != 3 {
		t.Errorf("three-word phrase tokenCount=%d, want 3", got3.tokenCount)
	}
	// Bounds union: left=10, top=5, right=155 (110+45), bottom=19 (5+14).
	if got3.left != 10 || got3.top != 5 || got3.width != 145 || got3.height != 14 {
		t.Errorf("phrase bounds = (%d,%d,%dx%d), want (10,5,145x14)", got3.left, got3.top, got3.width, got3.height)
	}
	// Confidence = min(0.95, 0.93, 0.91) = 0.91.
	if got3.confidence < 0.909 || got3.confidence > 0.911 {
		t.Errorf("phrase confidence=%v, want ~0.91 (min of constituents)", got3.confidence)
	}
	// Line-boundary respect: no phrase should contain "Other".
	for _, p := range phrases {
		if strings.Contains(p.text, "Other") {
			t.Errorf("phrase %q crosses line boundary into next-line token", p.text)
		}
	}

	// (d) Regex with `\s` whitespace pattern must match across tokens.
	//     Build a tiny image-free path by exercising the matcher logic
	//     via FindText with synthetic words is awkward; instead we
	//     verify the regex matches the phrase text generated above.
	re := regexp.MustCompile(`(?i)Loaded\s+skill`)
	if !re.MatchString(got3.text) {
		t.Errorf("regex 'Loaded\\s+skill' did not match phrase text %q", got3.text)
	}
	// Also confirm the regex would not match against token-only text
	// (the bug pre-task-15): no single token contains "Loaded skill".
	for _, w := range words {
		if re.MatchString(w.text) {
			t.Errorf("token %q unexpectedly matched 'Loaded\\s+skill' (token-only regex should miss)", w.text)
		}
	}
}

// TestFindText_SmartPSMRetry exercises the v0.3.1 auto-retry path
// (task-30). Strategy: seed the batch OCR cache so runOCR returns
// synthetic ocrWords without invoking tesseract. We pin two cache
// entries against the same prepped image — psm=0 returns no words,
// psm=11 returns the target token — and assert FindText surfaces
// psm_retried=true / psm_used=11 on the resulting candidate.
//
// Why bypass tesseract: real Tesseract output for a given fixture is
// not stable enough across tesseract versions to guarantee
// "default PSM misses, psm=11 hits"; the cache-seed approach pins the
// exact behavior the retry path must handle.
func TestFindText_SmartPSMRetry(t *testing.T) {
	if !tesseractAvailable() {
		// runOCR still LookPath()s tesseract before consulting cache.
		t.Skip("tesseract not on PATH")
	}
	// Small region (200*60 = 12_000 px², well below the 100_000 px²
	// gate). Pixels are all white so preprocessImage("auto") returns
	// applied="none" and `prepped` is the same buffer — making the
	// cache hash deterministic from the input image alone.
	region := contract.Bounds{X: 50, Y: 100, Width: 200, Height: 60}
	img := image.NewRGBA(image.Rect(0, 0, 200, 60))
	for y := 0; y < 60; y++ {
		for x := 0; x < 200; x++ {
			img.SetRGBA(x, y, color.RGBA{255, 255, 255, 255})
		}
	}
	bctx := NewBatchContext()
	hash := hashImage(region, img)
	// psm=0 → no words (forces the gate to fire).
	bctx.ocr["eng|none|"+hash+"|psm=0|oem=0"] = ocrEntry{words: nil}
	// psm=11 → one synthetic word matching the query "OK".
	bctx.ocr["eng|none|"+hash+"|psm=11|oem=0"] = ocrEntry{words: []ocrWord{
		{text: "OK", left: 20, top: 10, width: 40, height: 30, confidence: 0.92, block: 1, par: 1, line: 1},
	}}

	res, err := FindText(context.Background(), bctx, region, img, FindTextRequest{Query: "OK"})
	if err != nil {
		t.Fatalf("FindText error: %v", err)
	}
	if len(res.Candidates) != 1 {
		t.Fatalf("got %d candidates, want 1 (auto-retry should surface psm=11 result)", len(res.Candidates))
	}
	got := res.Candidates[0]
	if retried, _ := got.Extra["psm_retried"].(bool); !retried {
		t.Errorf("psm_retried = %v, want true (extra=%v)", got.Extra["psm_retried"], got.Extra)
	}
	if used, _ := got.Extra["psm_used"].(int); used != 11 {
		t.Errorf("psm_used = %v, want 11 (extra=%v)", got.Extra["psm_used"], got.Extra)
	}
	// Bounds must be region-relative-shifted into screen space.
	if got.Bounds.X != region.X+20 || got.Bounds.Y != region.Y+10 {
		t.Errorf("bounds = %+v, want region+local offset (X=%d, Y=%d)", got.Bounds, region.X+20, region.Y+10)
	}
}

// TestFindText_SmartPSMRetry_GateNegatives covers the cases where the
// auto-retry MUST NOT fire: explicit psm, and large region. We assert
// the retry didn't happen by checking that no second cache entry was
// touched and that no psm_retried marker appears on any candidate.
func TestFindText_SmartPSMRetry_GateNegatives(t *testing.T) {
	if !tesseractAvailable() {
		t.Skip("tesseract not on PATH")
	}

	// Case A: caller passes explicit psm — retry disabled by contract.
	t.Run("explicit_psm_disables_retry", func(t *testing.T) {
		region := contract.Bounds{X: 0, Y: 0, Width: 200, Height: 60}
		img := image.NewRGBA(image.Rect(0, 0, 200, 60))
		for y := 0; y < 60; y++ {
			for x := 0; x < 200; x++ {
				img.SetRGBA(x, y, color.RGBA{255, 255, 255, 255})
			}
		}
		bctx := NewBatchContext()
		hash := hashImage(region, img)
		// Caller asked for psm=6 explicitly. Seed only that cache key
		// with no words. Seeing the retry would require a psm=11 entry
		// which is intentionally absent — if the retry fires it would
		// shell out to tesseract (and the result would still be empty
		// on this blank fixture, but we'd see OCRCalls increment).
		bctx.ocr["eng|none|"+hash+"|psm=6|oem=0"] = ocrEntry{words: nil}
		callsBefore := bctx.OCRCalls()
		res, err := FindText(context.Background(), bctx, region, img, FindTextRequest{Query: "OK", PSM: 6})
		if err != nil {
			t.Fatalf("FindText error: %v", err)
		}
		if len(res.Candidates) != 0 {
			t.Errorf("expected 0 candidates on blank fixture, got %d", len(res.Candidates))
		}
		// No tesseract invocation should have happened (cache hit on
		// the psm=6 entry, no retry).
		if got := bctx.OCRCalls() - callsBefore; got != 0 {
			t.Errorf("OCRCalls delta = %d, want 0 (explicit psm must not trigger retry)", got)
		}
	})

	// Case B: large region — area >= smartPSMRegionThresholdPx, so
	// retry is gated off even when the first pass returns nothing.
	t.Run("large_region_disables_retry", func(t *testing.T) {
		// 400x300 = 120_000 px² > 100_000 threshold.
		region := contract.Bounds{X: 0, Y: 0, Width: 400, Height: 300}
		img := image.NewRGBA(image.Rect(0, 0, 400, 300))
		for y := 0; y < 300; y++ {
			for x := 0; x < 400; x++ {
				img.SetRGBA(x, y, color.RGBA{255, 255, 255, 255})
			}
		}
		bctx := NewBatchContext()
		hash := hashImage(region, img)
		bctx.ocr["eng|none|"+hash+"|psm=0|oem=0"] = ocrEntry{words: nil}
		callsBefore := bctx.OCRCalls()
		res, err := FindText(context.Background(), bctx, region, img, FindTextRequest{Query: "OK"})
		if err != nil {
			t.Fatalf("FindText error: %v", err)
		}
		if len(res.Candidates) != 0 {
			t.Errorf("expected 0 candidates on blank large fixture, got %d", len(res.Candidates))
		}
		if got := bctx.OCRCalls() - callsBefore; got != 0 {
			t.Errorf("OCRCalls delta = %d, want 0 (large region must not trigger retry)", got)
		}
	})
}

