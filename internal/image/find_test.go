// loadFixturePNG reads a PNG from testdata into an *image.RGBA.
func loadFixturePNG(t *testing.T, name string) *image.RGBA {
	t.Helper()
	f, err := os.Open(filepath.Join("testdata", name))
	if err != nil {
		t.Fatalf("open fixture %s: %v", name, err)
	}
	defer func() { _ = f.Close() }()
	src, err := png.Decode(f)
	if err != nil {
		t.Fatalf("decode fixture %s: %v", name, err)
	}
	b := src.Bounds()
	dst := image.NewRGBA(image.Rect(0, 0, b.Dx(), b.Dy()))
	draw.Draw(dst, dst.Bounds(), src, b.Min, draw.Src)
	return dst
}

func tesseractAvailable() bool {
	_, err := exec.LookPath("tesseract")
	return err == nil
}

func TestFindText_HappyPath(t *testing.T) {
	if !tesseractAvailable() {
		t.Skip("tesseract not on PATH")
	}
	img := loadFixturePNG(t, "hello_world.png")
	region := contract.Bounds{X: 0, Y: 0, Width: img.Bounds().Dx(), Height: img.Bounds().Dy()}
	res, err := FindText(context.Background(), nil, region, img, FindTextRequest{Query: "hello"})
	if err != nil {
		t.Fatalf("FindText error: %v", err)
	}
	if len(res.Candidates) == 0 {
		t.Fatalf("expected at least one candidate for 'hello', got 0")
	}
	if res.CoordSpace != contract.CoordSpaceScreen {
		t.Errorf("CoordSpace = %q, want %q", res.CoordSpace, contract.CoordSpaceScreen)
	}
	if res.Candidates[0].Source != contract.FindSourceOCR {
		t.Errorf("source = %q, want ocr", res.Candidates[0].Source)
	}
	// Bounds should sit inside the search region.
	c := res.Candidates[0]
	if !region.Contains(c.Bounds.X, c.Bounds.Y) {
		t.Errorf("candidate %+v outside region %+v", c.Bounds, region)
	}
	if c.Confidence <= 0 {
		t.Errorf("confidence should be > 0, got %v", c.Confidence)
	}
}

