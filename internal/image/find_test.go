// TestFindTextDarkTheme is the headline task-8 fixture test. It
// verifies the auto-invert pipeline activates on a synthetic dark-mode
// calculator readout ("40" in white on #1f1f1f) — the same shape as
// the failing v0.2 gnome-calculator smoke.
//
// Fixtures are generated by a tiny helper program kept in this repo
// alongside this test (see comment at the top of
// internal/image/testdata for regeneration instructions). They are
// hand-crafted bitmap glyphs (5x7 at scale 5 == 35px tall) — no
// external Go dependencies.
func TestFindTextDarkTheme(t *testing.T) {
	if !tesseractAvailable() {
		t.Skip("tesseract not on PATH")
	}
	dark := loadFixturePNG(t, "dark_calc_display.png")
	light := loadFixturePNG(t, "light_calc_display.png")
	darkRegion := contract.Bounds{X: 0, Y: 0, Width: dark.Bounds().Dx(), Height: dark.Bounds().Dy()}
	lightRegion := contract.Bounds{X: 0, Y: 0, Width: light.Bounds().Dx(), Height: light.Bounds().Dy()}

	// (a) auto on dark fixture → at least one candidate, applied=invert.
	res, err := FindText(context.Background(), nil, darkRegion, dark, FindTextRequest{Query: "40", Preprocess: "auto"})
	if err != nil {
		t.Fatalf("auto FindText (dark) error: %v", err)
	}
	if len(res.Candidates) == 0 {
		t.Fatalf("auto on dark fixture: expected >=1 candidate, got 0")
	}
	if got, _ := res.Candidates[0].Extra["preprocess"].(string); got != "invert" {
		t.Errorf("auto on dark fixture: applied=%q, want %q", got, "invert")
	}
	if c := res.Candidates[0].Confidence; c <= 0.5 {
		t.Errorf("auto on dark fixture: top confidence %.3f, want > 0.5", c)
	}

	// (b) auto on light fixture → applied=none (luminance above threshold).
	res2, err := FindText(context.Background(), nil, lightRegion, light, FindTextRequest{Query: "40", Preprocess: "auto"})
	if err != nil {
		t.Fatalf("auto FindText (light) error: %v", err)
	}
	if len(res2.Candidates) == 0 {
		t.Fatalf("auto on light fixture: expected >=1 candidate, got 0")
	}
	if got, _ := res2.Candidates[0].Extra["preprocess"].(string); got != "none" {
		t.Errorf("auto on light fixture: applied=%q, want %q (no inversion on light bg)", got, "none")
	}

	// (c) preprocess=none on dark fixture → applied=none (opt-out works).
	res3, err := FindText(context.Background(), nil, darkRegion, dark, FindTextRequest{Query: "40", Preprocess: "none"})
	if err != nil {
		t.Fatalf("none FindText (dark) error: %v", err)
	}
	for _, c := range res3.Candidates {
		if got, _ := c.Extra["preprocess"].(string); got != "none" {
			t.Errorf("preprocess=none on dark fixture: applied=%q, want %q (must not auto-invert)", got, "none")
		}
	}

	// (d) preprocess=invert on light fixture → applied=invert
	// (proves explicit-invert path is honored regardless of luminance).
	res4, err := FindText(context.Background(), nil, lightRegion, light, FindTextRequest{Query: "40", Preprocess: "invert"})
	if err != nil {
		t.Fatalf("invert FindText (light) error: %v", err)
	}
	if len(res4.Candidates) == 0 {
		t.Fatalf("explicit invert on light fixture: expected >=1 candidate, got 0")
	}
	if got, _ := res4.Candidates[0].Extra["preprocess"].(string); got != "invert" {
		t.Errorf("explicit invert on light fixture: applied=%q, want %q", got, "invert")
	}
}

// TestPreprocessImage_AutoDetectsDark unit-tests the luminance scanner
// without invoking tesseract. Synthesized 32x32 buffers: one all-dark,
// one all-light, one mixed at the threshold.
func TestPreprocessImage_AutoDetectsDark(t *testing.T) {
	mk := func(r, g, b uint8) *image.RGBA {
		im := image.NewRGBA(image.Rect(0, 0, 32, 32))
		for y := 0; y < 32; y++ {
			for x := 0; x < 32; x++ {
				im.SetRGBA(x, y, color.RGBA{r, g, b, 255})
			}
		}
		return im
	}
	cases := []struct {
		name    string
		img     *image.RGBA
		want    string
		wantInv bool // expect pixel inversion to have occurred
	}{
		{"all-dark", mk(0x1f, 0x1f, 0x1f), "invert", true},
		{"all-light", mk(0xff, 0xff, 0xff), "none", false},
		{"medium-gray-above-threshold", mk(120, 120, 120), "none", false},
	}
	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			out, applied := preprocessImage(tc.img, "auto")
			if applied != tc.want {
				t.Errorf("applied=%q, want %q", applied, tc.want)
			}
			top := out.RGBAAt(0, 0)
			src := tc.img.RGBAAt(0, 0)
			inverted := top.R != src.R || top.G != src.G || top.B != src.B
			if inverted != tc.wantInv {
				t.Errorf("inversion happened=%v, want %v", inverted, tc.wantInv)
			}
		})
	}
}

// TestPreprocessImage_ExplicitModes covers invert/binarize/none modes
// without tesseract.
func TestPreprocessImage_ExplicitModes(t *testing.T) {
	img := image.NewRGBA(image.Rect(0, 0, 4, 4))
	for y := 0; y < 4; y++ {
		for x := 0; x < 4; x++ {
			img.SetRGBA(x, y, color.RGBA{50, 100, 200, 255})
		}
	}
	inv, applied := preprocessImage(img, "invert")
	if applied != "invert" {
		t.Errorf("applied=%q, want invert", applied)
	}
	if got := inv.RGBAAt(0, 0); got.R != 205 || got.G != 155 || got.B != 55 || got.A != 255 {
		t.Errorf("invert pixel = %+v, want {205,155,55,255}", got)
	}
	none, applied := preprocessImage(img, "none")
	if applied != "none" || none.RGBAAt(0, 0) != img.RGBAAt(0, 0) {
		t.Errorf("none mode mutated pixels or label (applied=%q)", applied)
	}
	// Binarize a half-dark / half-light buffer: Otsu should split cleanly.
	bin := image.NewRGBA(image.Rect(0, 0, 4, 4))
	for y := 0; y < 4; y++ {
		for x := 0; x < 4; x++ {
			if x < 2 {
				bin.SetRGBA(x, y, color.RGBA{0, 0, 0, 255})
			} else {
				bin.SetRGBA(x, y, color.RGBA{255, 255, 255, 255})
			}
		}
	}
	bout, applied := preprocessImage(bin, "binarize")
	if applied != "binarize" {
		t.Errorf("applied=%q, want binarize", applied)
	}
	if got := bout.RGBAAt(0, 0); got.R != 0 {
		t.Errorf("dark pixel after binarize: R=%d, want 0", got.R)
	}
	if got := bout.RGBAAt(3, 0); got.R != 255 {
		t.Errorf("light pixel after binarize: R=%d, want 255", got.R)
	}
}

